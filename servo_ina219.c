uint16_t INA219_read_regs(uint8_t reg) {
	uint8_t data[2];
	uint16_t value = 0;
	HAL_I2C_Mem_Read(&hi2c1, INA219_ADDR, reg, I2C_MEMADD_SIZE_8BIT, data, 2, 100);
	value = (data[0] << 8) | data[1];
	return value;
}

float INA219_current() {
	int16_t val = INA219_read_regs(0x04);
	return val * 0.1;
}

// rn timer is firing every 10us --> 1ms = 0, 2ms = 180
void set_servo(uint8_t angle) {
	uint32_t pulse = 1000 + ((angle * (2000-1000))/180);
	__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, pulse);
}

void I2C_Scan(void) {
    char buffer[50];
    sprintf(buffer, "Scanning I2C bus...\r\n");
    HAL_UART_Transmit(&huart2, (uint8_t*)buffer, strlen(buffer), HAL_MAX_DELAY);

    for (uint8_t addr = 1; addr < 128; addr++) {
        // HAL_I2C_IsDeviceReady expects the address shifted left by 1.
        if (HAL_I2C_IsDeviceReady(&hi2c1, addr << 1, 1, 100) == HAL_OK) {
            sprintf(buffer, "Device found at 0x%02X\r\n", addr);
            HAL_UART_Transmit(&huart2, (uint8_t*)buffer, strlen(buffer), HAL_MAX_DELAY);
        }
    }
    sprintf(buffer, "I2C scan complete.\r\n");
    HAL_UART_Transmit(&huart2, (uint8_t*)buffer, strlen(buffer), HAL_MAX_DELAY);
}
